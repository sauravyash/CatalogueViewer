    private boolean validateEmail(String emailStr) {
        Pattern VALID_EMAIL_ADDRESS_REGEX = 
            Pattern.compile("^[A-Z0-9._%+-]+@[A-Z0-9.-]+\\.[A-Z]{2,6}$", Pattern.CASE_INSENSITIVE);
        Matcher matcher = VALID_EMAIL_ADDRESS_REGEX.matcher(emailStr);
        return matcher.find();
    }
    
    private void RegisterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterButtonActionPerformed
        String fn = RegisterFirstNameField.getText();
        String ln = RegisterLastNameField.getText();
        String email = RegisterEmailField.getText();
        String username = RegisterUserNameField.getText();
        String pwd = RegisterPasswordField.getText();
        
        try {
            if (fn.length() < 2) {
                throw new IllegalArgumentException("fn");
            } else if (ln.length() < 2) {
                throw new IllegalArgumentException("ln");
            } else if ((email.length() < 5) || !validateEmail(email)) {
                throw new IllegalArgumentException("email");
            } else if (username.length() < 3) {
                throw new IllegalArgumentException("username");
            } else if (pwd.length() < 6) {
                throw new IllegalArgumentException("pwd");
            }
            
            UserDatabase.AddUser(fn, ln, email, username, pwd);
            RegisterSuccessLabel.setVisible(true);
            RegisterFailureLabel.setVisible(false);
            
            
            Utilities.setTimeout(() -> OpenLoginPage(), 2);
           
        } 
        catch (UserAlreadyExistsException e) {
            RegisterSuccessLabel.setVisible(false);
            RegisterFailureLabel.setVisible(true);
            RegisterFailureLabel.setText("The Username chosen is not available. Please use another username.");
            System.out.println("Registration Failure: "+ e);
        } 
        catch (IllegalArgumentException e) {
            RegisterSuccessLabel.setVisible(false);
            RegisterFailureLabel.setVisible(true);
            if(e.getMessage() !=  null) switch (e.getMessage()) {
                case "fn":
                    RegisterFailureLabel.setText("First name is invalid!");
                    break;
                case "ln":
                    RegisterFailureLabel.setText("Last name is invalid!");
                    break;
                case "email":
                    RegisterFailureLabel.setText("Email is invalid!");
                    break;
                case "username":
                    RegisterFailureLabel.setText("Username is unavailable!");
                    break;
                case "pwd":
                    RegisterFailureLabel.setText("Password is not allowed!");
                    break;
                default:
                    break;
            }
        } catch (Exception e){
            RegisterSuccessLabel.setVisible(false);
            RegisterFailureLabel.setVisible(true);
            System.out.println("Registration Failure: "+ e );
            RegisterFailureLabel.setText("Registeration was unsuccessful!");
        }
        
        
    }//GEN-LAST:event_RegisterButtonActionPerformed

