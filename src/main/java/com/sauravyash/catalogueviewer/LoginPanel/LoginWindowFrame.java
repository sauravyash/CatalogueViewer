    private void Login(){
        String username = LoginUsernameField.getText();
        String pwd = LoginPasswordField.getText();
        
        if (UserDatabase.IsUserValid(username, pwd)){
            LoginInvalidLabel.setVisible(false);
            System.out.println("login successful");
            CatalogueViewer.OpenSelectorWindow();
            this.dispose();
        } else {
            LoginInvalidLabel.setVisible(true);
            System.out.println("login not very succ");
        }
    }

